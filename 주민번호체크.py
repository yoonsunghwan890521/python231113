import re

def check_resident_registration_number(rrn):
    # 정규 표현식 패턴을 정의합니다.
    # - ^(\d{6})-?[1-2]\d{6}$:
    #   ^(\d{6}): 문자열의 시작과 첫 6자리(년, 월, 일)를 캡처합니다.
    #   -?: 옵션으로 하이픈을 매칭합니다.
    #   [1-2]: 마지막 7자리의 첫 번째 숫자는 1 또는 2여야 합니다.
    #   \d{6}$: 마지막 부분의 나머지 6자리를 매칭합니다.
    pattern = r'^(\d{6})-?[1-2]\d{6}$'
    
    # search() 함수를 사용하여 주민등록번호가 패턴과 일치하는지 확인합니다.
    match = re.search(pattern, rrn)
    
    # 일치하는 경우 주민등록번호가 유효하고, 그렇지 않은 경우 유효하지 않습니다.
    return bool(match)

# 10개의 주민등록번호를 테스트합니다.
rrns_to_test = [
    '900101-1234567',
    '950212-2345678',
    '9803311234567',
    '880430-3456789',
    '811201-4567890',
    '930505-1122334',
    '891213-1234567',
    '940706-2121212',
    '990809-1122334',
    '8712311234567'
]

# 주민등록번호 목록을 반복하며 유효성을 검사하고 결과를 출력합니다.
for rrn in rrns_to_test:
    if check_resident_registration_number(rrn):
        print(f'{rrn}는 유효한 주민등록번호입니다.')
    else:
        print(f'{rrn}는 유효하지 않은 주민등록번호입니다.')
